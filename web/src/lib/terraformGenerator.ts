/** Sanitize a name to fit Terraform identifier rules (`[a-z_][a-z0-9_-]*`). */
export function sanitizeName(raw: string): string {
  let s = raw.toLowerCase().replace(/[^a-z0-9_-]+/g, '_').replace(/^_+|_+$/g, '');
  if (!s) s = 'res';
  if (/^[0-9]/.test(s)) s = `r_${s}`;
  return s;
}

function quote(v: unknown): string {
  if (v === null || v === undefined) return '""';
  return JSON.stringify(String(v));
}

/** Defensive cast — returns `value` if it`s an array, otherwise an empty array. */
function asArray<T>(v: unknown): T[] {
  return Array.isArray(v) ? (v as T[]) : [];
}

/** Defensive cast — returns `value` if it`s a plain object, otherwise `{}`. */
function asObj(v: unknown): Record<string, unknown> {
  return v && typeof v === 'object' && !Array.isArray(v) ? (v as Record<string, unknown>) : {};
}

function tagsBlock(r: ArgResource): string {
  if (!r.tags || Object.keys(r.tags).length === 0) return '';
  const lines = Object.entries(r.tags).map(([k, v]) => `  ${quote(k)} = ${quote(v)}`);
  return `\n  tags = {\n${lines.join('\n')}\n  }`;
}

/** Build the `idMap` so renderers can refer to other resources via tf address. */
function buildIdMap(resources: ArgResource[]): Map<string, string> {
  const m = new Map<string, string>();
  // Always include the synthetic RG block.
  const seenRg = new Set<string>();
  for (const r of resources) {
    seenRg.add(r.resourceGroup.toLowerCase());
  }
  for (const rg of seenRg) {
    const rgId =
      `/subscriptions/${resources[0]?.subscriptionId ?? '<sub>'}/resourceGroups/${rg}`.toLowerCase();
    m.set(rgId, `azurerm_resource_group.${sanitizeName(rg)}`);
  }
  for (const r of resources) {
    const tfType = TF_TYPE_OF[r.type.toLowerCase()];
    if (tfType) {
      // For subnets the resource name is "vnet/subnet" but the block name is
      // just the subnet leaf, so the idMap must use the leaf too.
      const isSubnet = r.type.toLowerCase() === 'microsoft.network/virtualnetworks/subnets';
      const blockName = isSubnet ? (r.name.split('/').pop() ?? r.name) : r.name;
      m.set(r.id.toLowerCase(), `${tfType}.${sanitizeName(blockName)}`);
    }
  }
  return m;
}

/**
 * Resolve a hardcoded ARM id to a Terraform reference *if* the target is in
 * scope. Falls back to a JSON-quoted literal so the user can fix it manually.
 */
function refOrLiteral(armId: string, ctx: RenderContext, suffix = '.id'): string {
  const ref = ctx.idMap.get(armId.toLowerCase());
  if (ref) return `${ref}${suffix}`;
  return quote(armId);
}

