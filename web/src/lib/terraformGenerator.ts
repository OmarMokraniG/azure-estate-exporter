function renderVm(r: ArgResource, ctx: RenderContext): RenderResult {
  const p = asObj(r.properties);
  const osProfile = asObj(p.osProfile);
  const isWindows = Boolean(osProfile.windowsConfiguration);
  const tfType = isWindows ? 'azurerm_windows_virtual_machine' : 'azurerm_linux_virtual_machine';
  const tfName = sanitizeName(r.name);
  const hwProfile = asObj(p.hardwareProfile);
  const networkProfile = asObj(p.networkProfile);
  const nics = asArray<{ id?: string }>(networkProfile.networkInterfaces)
    .map((n) => (n.id ? refOrLiteral(n.id, ctx) : '"<TODO: nic id>"'))
    .join(', ');
  const storageProfile = asObj(p.storageProfile);
  const imageRef = asObj(storageProfile.imageReference);
  const osDisk = asObj(storageProfile.osDisk);
  const sourceImageBlock = imageRef.publisher
    ? `\n  source_image_reference {
    publisher = ${quote(imageRef.publisher)}
    offer     = ${quote(imageRef.offer)}
    sku       = ${quote(imageRef.sku)}
    version   = ${quote(imageRef.version ?? 'latest')}
  }`
    : '';
  const credBlock = isWindows
    ? `\n  admin_password = var.admin_password`
    : `\n  admin_ssh_key {
    username   = var.admin_username
    public_key = var.admin_ssh_public_key
  }`;
  const hcl = `resource "${tfType}" "${tfName}" {
  name                  = ${quote(r.name)}
  resource_group_name   = azurerm_resource_group.${sanitizeName(r.resourceGroup)}.name
  location              = ${quote(r.location)}
  size                  = ${quote(hwProfile.vmSize ?? 'Standard_B2s')}
  admin_username        = ${quote(osProfile.adminUsername ?? 'azureuser')}
  network_interface_ids = [${nics}]

  os_disk {
    name                 = ${quote(osDisk.name ?? `${r.name}-osdisk`)}
    caching              = ${quote(osDisk.caching ?? 'ReadWrite')}
    storage_account_type = ${quote(asObj(osDisk.managedDisk).storageAccountType ?? 'StandardSSD_LRS')}
  }${sourceImageBlock}
  # ⚠ password / SSH key NOT exported — set via terraform.tfvars before apply.${credBlock}${tagsBlock(r)}
}`;
  return { hcl, tfType, tfName };
}

