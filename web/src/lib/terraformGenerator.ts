function renderResourceGroup(rgName: string, location: string, tags?: Record<string, string>): string {
  const tagsHcl =
    tags && Object.keys(tags).length
      ? `\n  tags = {\n${Object.entries(tags)
          .map(([k, v]) => `    ${quote(k)} = ${quote(v)}`)
          .join('\n')}\n  }`
      : '';
  return `resource "azurerm_resource_group" "${sanitizeName(rgName)}" {
  name     = ${quote(rgName)}
  location = ${quote(location)}${tagsHcl}
}`;
}

function renderStorageAccount(r: ArgResource): RenderResult {
  const p = (r.properties ?? {}) as Record<string, unknown>;
  const sku = (r.sku?.name ?? 'Standard_LRS').split('_');
  const tier = sku[0] ?? 'Standard';
  const repl = sku[1] ?? 'LRS';
  const tfName = sanitizeName(r.name);
  const hcl = `resource "azurerm_storage_account" "${tfName}" {
  name                     = ${quote(r.name)}
  resource_group_name      = azurerm_resource_group.${sanitizeName(r.resourceGroup)}.name
  location                 = ${quote(r.location)}
  account_tier             = ${quote(tier)}
  account_replication_type = ${quote(repl)}
  account_kind             = ${quote(r.kind ?? 'StorageV2')}
  min_tls_version          = ${quote(p.minimumTlsVersion ?? 'TLS1_2')}
  https_traffic_only_enabled = ${p.supportsHttpsTrafficOnly === false ? 'false' : 'true'}
  allow_nested_items_to_be_public = ${p.allowBlobPublicAccess === true ? 'true' : 'false'}
  public_network_access_enabled   = ${p.publicNetworkAccess === 'Disabled' ? 'false' : 'true'}${tagsBlock(r)}
}`;
  return { hcl, tfType: 'azurerm_storage_account', tfName };
}

function renderVirtualNetwork(r: ArgResource, ctx: RenderContext): RenderResult {
  const p = asObj(r.properties);
  const addrSpace = asArray<string>(asObj(p.addressSpace).addressPrefixes);
  const prefixes = addrSpace.length ? addrSpace : ['10.0.0.0/16'];
  const dns = asArray<string>(asObj(p.dhcpOptions).dnsServers);
  const tfName = sanitizeName(r.name);
  const dnsLine = dns.length ? `\n  dns_servers         = [${dns.map(quote).join(', ')}]` : '';
  const hcl = `resource "azurerm_virtual_network" "${tfName}" {
  name                = ${quote(r.name)}
  resource_group_name = azurerm_resource_group.${sanitizeName(r.resourceGroup)}.name
  location            = ${quote(r.location)}
  address_space       = [${prefixes.map(quote).join(', ')}]${dnsLine}${tagsBlock(r)}
}`;
  void ctx;
  return { hcl, tfType: 'azurerm_virtual_network', tfName };
}

function renderSubnet(r: ArgResource, ctx: RenderContext): RenderResult {
  const p = asObj(r.properties);
  const prefixes =
    asArray<string>(p.addressPrefixes).length > 0
      ? asArray<string>(p.addressPrefixes)
      : typeof p.addressPrefix === 'string'
        ? [p.addressPrefix]
        : [];
  const leaf = r.name.split('/').pop() ?? r.name;
  const tfName = sanitizeName(leaf);
  const vnetId = r.id.replace(/\/subnets\/[^/]+$/i, '');
  const vnetRef = ctx.idMap.get(vnetId.toLowerCase());
  const vnetLine = vnetRef
    ? `${vnetRef}.name`
    : quote((vnetId.split('/').pop() ?? '<vnet>') as string);
  const hcl = `resource "azurerm_subnet" "${tfName}" {
  name                 = ${quote(leaf)}
  resource_group_name  = azurerm_resource_group.${sanitizeName(r.resourceGroup)}.name
  virtual_network_name = ${vnetLine}
  address_prefixes     = [${prefixes.map(quote).join(', ')}]
}`;
  return { hcl, tfType: 'azurerm_subnet', tfName };
}

function renderNic(r: ArgResource, ctx: RenderContext): RenderResult {
  const p = asObj(r.properties);
  const ipcs = asArray<Record<string, unknown>>(p.ipConfigurations).map((ipc, i) => {
    const ipcProps = asObj(ipc.properties);
    const subnetId = (asObj(ipcProps.subnet).id as string | undefined) ?? undefined;
    const pipId = (asObj(ipcProps.publicIPAddress).id as string | undefined) ?? undefined;
    const alloc = ipcProps.privateIPAllocationMethod ?? 'Dynamic';
    const pipLine = pipId ? `\n    public_ip_address_id          = ${refOrLiteral(pipId, ctx)}` : '';
    return `  ip_configuration {
    name                          = ${quote((ipc.name as string) ?? `ipconfig${i + 1}`)}
    subnet_id                     = ${subnetId ? refOrLiteral(subnetId, ctx) : '"<TODO: subnet id>"'}
    private_ip_address_allocation = ${quote(alloc)}${pipLine}
  }`;
  });
  const tfName = sanitizeName(r.name);
  const hcl = `resource "azurerm_network_interface" "${tfName}" {
  name                = ${quote(r.name)}
  resource_group_name = azurerm_resource_group.${sanitizeName(r.resourceGroup)}.name
  location            = ${quote(r.location)}

${ipcs.join('\n\n')}${tagsBlock(r)}
}`;
  return { hcl, tfType: 'azurerm_network_interface', tfName };
}

function renderNsg(r: ArgResource): RenderResult {
  const p = asObj(r.properties);
  const rules = asArray<Record<string, unknown>>(p.securityRules).map((rule) => {
    const rp = asObj(rule.properties);
    const lines = [
      `    name                       = ${quote(rule.name as string)}`,
      `    priority                   = ${rp.priority}`,
      `    direction                  = ${quote(rp.direction)}`,
      `    access                     = ${quote(rp.access)}`,
      `    protocol                   = ${quote(rp.protocol)}`,
      `    source_port_range          = ${quote(rp.sourcePortRange ?? '*')}`,
      `    destination_port_range     = ${quote(rp.destinationPortRange ?? '*')}`,
      `    source_address_prefix      = ${quote(rp.sourceAddressPrefix ?? '*')}`,
      `    destination_address_prefix = ${quote(rp.destinationAddressPrefix ?? '*')}`,
    ];
    return `  security_rule {\n${lines.join('\n')}\n  }`;
  });
  const tfName = sanitizeName(r.name);
  const hcl = `resource "azurerm_network_security_group" "${tfName}" {
  name                = ${quote(r.name)}
  resource_group_name = azurerm_resource_group.${sanitizeName(r.resourceGroup)}.name
  location            = ${quote(r.location)}

${rules.join('\n\n')}${tagsBlock(r)}
}`;
  return { hcl, tfType: 'azurerm_network_security_group', tfName };
}

