function renderWebApp(r: ArgResource, ctx: RenderContext): RenderResult {
  const p = (r.properties ?? {}) as Record<string, unknown>;
  const kind = (r.kind ?? '').toLowerCase();
  const isWindows = kind.includes('app') && !kind.includes('linux');
  const tfType = isWindows ? 'azurerm_windows_web_app' : 'azurerm_linux_web_app';
  const tfName = sanitizeName(r.name);
  const planId = p.serverFarmId as string | undefined;
  const planRef = planId ? refOrLiteral(planId, ctx) : '"<TODO: serverFarmId>"';
  const hcl = `resource "${tfType}" "${tfName}" {
  name                = ${quote(r.name)}
  resource_group_name = azurerm_resource_group.${sanitizeName(r.resourceGroup)}.name
  location            = ${quote(r.location)}
  service_plan_id     = ${planRef}

  site_config {
    always_on = ${p.alwaysOn === false ? 'false' : 'true'}
  }${tagsBlock(r)}
}`;
  return { hcl, tfType, tfName };
}

