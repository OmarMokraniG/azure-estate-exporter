/**
 * Browser-side Terraform HCL generator.
 *
 * Given the `ArgResource[]` already in the SPA, produce a complete file tree
 * (Map<path, content>) that mirrors what the PowerShell module would emit:
 *
 *   README.md
 *   .gitignore
 *   backend.tf.example
 *   docs/coverage.md
 *   infra/<rg>/
 *     main.tf
 *     provider.tf
 *     terraform.tf
 *     variables.tf
 *     terraform.tfvars.example
 *     outputs.tf
 *     README.md
 *
 * **Honesty**: this is a PREVIEW. The PowerShell module + `aztfexport`
 * remains the gold standard. The in-browser generator covers the ~15 most
 * common resource types in dev/test estates; for everything else it emits
 * an honest commented stub (`# TODO: ...`) plus a `moved` block hint so
 * users see exactly what is missing.
 */

import type { ArgResource } from '@/api/arm';

export interface GeneratedFile {
  path: string;
  content: string;
  language: 'hcl' | 'markdown' | 'json' | 'text';
}

export interface GenerateOptions {
  subscriptionId: string;
  subscriptionName?: string;
  resourceGroup?: string;
  resources: ArgResource[];
  generatorVersion?: string;
}

interface RenderContext {
  /** Map azure resource id (lowercased) → tf address ("type.name"). */
  idMap: Map<string, string>;
  /** Resource we are currently rendering. */
  resource: ArgResource;
}

interface RenderResult {
  /** The HCL block. Empty string when type is intentionally skipped. */
  hcl: string;
  /** Optional tf type for the outputs.tf entry (e.g. "azurerm_storage_account"). */
  tfType?: string;
  /** Optional tf name (e.g. "stcontoso"). */
  tfName?: string;
}

